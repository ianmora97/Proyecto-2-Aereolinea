    private void ModificarDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ModificarDatosMouseClicked
        // TODO add your handling code here:
        String nombre = "", 
                apellidos = "", 
                pass = "", 
                celular= "", 
                tel= "", 
                direccion= "", 
                correo = "", 
                fecha= "",
                id = "";
        try {
            id = userIdField.getText();
            nombre = nombreField.getText();
            apellidos = apellidosField.getText();
            correo = mailField.getText();
            pass = passField.getText();
            direccion = direccionField.getText();
            fecha = fechaField.getText();
            tel = TelField.getText();
            celular = celularField.getText();
        } catch (Exception e) {}
        
        Usuario user = new Usuario(id,pass,nombre,apellidos,correo,fecha,direccion,tel,celular);
        try {
            controller.modificarUsuarios(user);
            JOptionPane.showMessageDialog(this, "Datos modificados");
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(this, "NO se pudo Modificar");
        
        }
    }//GEN-LAST:event_ModificarDatosMouseClicked

